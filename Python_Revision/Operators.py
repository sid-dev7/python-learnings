n1 = 10
n2 = 20

# addition
addition = n1 + n2
print(f"addition = {addition}, datatype ={type(addition)}")

# subtraction
subtraction = n1 - n2
print(f"subtraction = {subtraction},datatype ={type(subtraction)}")

# True Division
Division = n1/n2
print(f"division = {Division}, datatype ={type(Division)}")
# Floor
Division = n1//n2

print(f"division = {Division}, datatype ={type(Division)}")

# multiplication
multiplication = n1*n2
print(f"multiplication = {multiplication}, datatype ={type(multiplication)}")

# power
Power = 10 ** 2
print(f"power={Power}, datatype ={type(Power)}")

# remainder (modulus)
mod = 10 % 2
print(f"mod ={mod}")